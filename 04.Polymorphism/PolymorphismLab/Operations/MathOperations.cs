using System;
using System.Collections.Generic;
using System.Text;

namespace Operations
{
    public class MathOperations
    {
        public int Add(int firstNum, int secondNum)
        {
            return firstNum + secondNum;
        }

        public double Add(double firstNum, double seconNum, double thirdNum)
        {
            return firstNum + seconNum + thirdNum;
        }

        public decimal Add(decimal firrstNum, decimal secondNum, decimal thirdNum)
        {
            return firrstNum + secondNum + thirdNum;
        }

    }
}
